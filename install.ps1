function ConvertTo-ASCIIArt {
    param([string]$Text)
    
    # Simple ASCII art font (similar to figlet slant)
    $font = @{
        'A' = @('  ___   ', ' / _ \  ', '/ /_\ \ ', '|  _  | ', '| | | | ', '\_| |_/ ')
        'B' = @(' ____  ', '| __ ) ', '|  _ \ ', '| |_) |', '|____/ ', '       ')
        'C' = @('  ____ ', ' / ___|', '| |    ', '| |___ ', ' \____|', '       ')
        'D' = @(' ____  ', '|  _ \ ', '| | | |', '| |_| |', '|____/ ', '       ')
        'E' = @(' _____ ', '| ____|', '|  _|  ', '| |___ ', '|_____|', '       ')
        'F' = @(' _____ ', '| ____|', '|  _|  ', '| |    ', '|_|    ', '       ')
        'G' = @('  ____ ', ' / ___|', '| |  _ ', '| |_| |', ' \____|', '       ')
        'H' = @(' _   _ ', '| | | |', '| |_| |', '|  _  |', '| | | |', '|_| |_|')
        'I' = @(' ___ ', '|_ _|', ' | | ', ' | | ', '|___|', '     ')
        'J' = @('     _ ', '    | |', ' _  | |', '| |_| |', ' \___/ ', '       ')
        'K' = @(' _  __', '| |/ /', '| '' / ', '| . \ ', '|_|\_\', '      ')
        'L' = @(' _     ', '| |    ', '| |    ', '| |___ ', '|_____|', '       ')
        'M' = @(' __  __ ', '|  \/  |', '| |\/| |', '| |  | |', '|_|  |_|', '        ')
        'N' = @(' _   _ ', '| \ | |', '|  \| |', '| |\  |', '|_| \_|', '       ')
        'O' = @('  ___  ', ' / _ \ ', '| | | |', '| |_| |', ' \___/ ', '       ')
        'P' = @(' ____  ', '|  _ \ ', '| |_) |', '|  __/ ', '|_|    ', '       ')
        'Q' = @('  ___  ', ' / _ \ ', '| | | |', '| |_| |', ' \__\_\', '       ')
        'R' = @(' ____  ', '|  _ \ ', '| |_) |', '|  _ < ', '|_| \_\', '       ')
        'S' = @(' ____  ', '/ ___| ', '\___ \ ', ' ___) |', '|____/ ', '       ')
        'T' = @(' _____ ', '|_   _|', '  | |  ', '  | |  ', '  |_|  ', '       ')
        'U' = @(' _   _ ', '| | | |', '| | | |', '| |_| |', ' \___/ ', '       ')
        'V' = @(' _   _ ', '| | | |', '| | | |', '| |_| |', ' \___/ ', '       ')
        'W' = @(' _    _ ', '| |  | |', '| |  | |', '| |/\| |', '|__/\__|', '        ')
        'X' = @(' __  __', ' \ \/ /', '  \  / ', '  /  \ ', ' /_/\_\', '       ')
        'Y' = @(' _   _ ', '| | | |', '| |_| |', ' \__, |', '   /_/ ', '       ')
        'Z' = @(' _____ ', '|__  / ', '  / /  ', ' / /_  ', '/____|', '       ')
        '-' = @('       ', '       ', ' _____ ', '|_____|', '       ', '       ')
        ' ' = @('   ', '   ', '   ', '   ', '   ', '   ')
    }
    
    $lines = @('', '', '', '', '', '')
    foreach ($char in $Text.ToUpper().ToCharArray()) {
        if ($font.ContainsKey([string]$char)) {
            for ($i = 0; $i -lt 6; $i++) {
                $lines[$i] += $font[[string]$char][$i]
            }
        }
    }
    
    return $lines -join "`n"
}

