shell() {
    case "$1" in
        install)
            local pkg="$2"
            local url=$(jq -r ".packages[\"$pkg\"].url" "$PACKAGES_FILE" 2>/dev/null)
            local type=$(jq -r ".packages[\"$pkg\"].type" "$PACKAGES_FILE" 2>/dev/null)
            
            if [ "$url" = "null" ]; then
                echo "рџ”Ќ Not in Aurora registry. Searching Homebrew..."
                if brew info "$pkg" &>/dev/null; then
                    echo "рџ“¦ Found in Homebrew: $pkg"
                    echo -n "Install via brew? (y/n): "
                    read confirm
                    [ "$confirm" = "y" ] && brew install "$pkg" && echo "вњ… Installed via Homebrew"
                else
                    echo "вќЊ Package not found in Aurora or Homebrew"
                fi
                return
            fi
            
            echo "рџ“¦ Installing $pkg..."
            case "$type" in
                dmg)
                    local tmp="/tmp/$pkg.dmg"
                    curl -L "$url" -o "$tmp"
                    hdiutil attach "$tmp" -quiet
                    local vol=$(ls /Volumes | grep -v Macintosh | head -1)
                    cp -R "/Volumes/$vol"/*.app ~/Applications/ 2>/dev/null
                    hdiutil detach "/Volumes/$vol" -quiet
                    rm "$tmp"
                    echo "вњ… Installed to ~/Applications"
                    ;;
                binary)
                    curl -L "$url" -o "$INSTALLED_DIR/$pkg"
                    chmod +x "$INSTALLED_DIR/$pkg"
                    echo "вњ… Installed to $INSTALLED_DIR"
                    ;;
                brew)
                    brew install "$pkg"
                    ;;
                cli-installer)
                    echo "рџ“¦ Installing Aurora-Shell CLI..."
                    cat > "$INSTALLED_DIR/CLI" << 'CLIEOF'
#!/bin/zsh
CLI_PACKAGES_FILE="$HOME/.aurora-shell_files/cli-packages.json"

if [ ! -f "$CLI_PACKAGES_FILE" ]; then
    cat > "$CLI_PACKAGES_FILE" << 'CLIPKG'
{
  "packages": {
    "GitHub": {
      "command": "gh",
      "install": "brew install gh",
      "description": "GitHub CLI - official GitHub command line tool"
    },
    "Microsoft.Teams": {
      "command": "teams-cli",
      "install": "echo 'Teams CLI not yet available - coming soon'",
      "description": "Microsoft Teams CLI (coming soon)"
    }
  }
}
CLIPKG
fi

case "$1" in
    install)
        pkg="$2"
        install_cmd=$(jq -r ".packages[\"$pkg\"].install" "$CLI_PACKAGES_FILE" 2>/dev/null)
        cmd_name=$(jq -r ".packages[\"$pkg\"].command" "$CLI_PACKAGES_FILE" 2>/dev/null)
        
        if [ "$install_cmd" = "null" ]; then
            echo "вќЊ CLI package '$pkg' not found"
            echo "Available: $(jq -r '.packages | keys[]' "$CLI_PACKAGES_FILE" 2>/dev/null | tr '\n' ' ')"
            exit 1
        fi
        
        echo "рџ“¦ Installing $pkg CLI..."
        eval "$install_cmd"
        
        if command -v "$cmd_name" &>/dev/null; then
            echo "вњ… $pkg CLI installed: $cmd_name"
        fi
        ;;
    list)
        echo "рџ“‹ Available CLI packages:"
        jq -r '.packages | to_entries[] | "  \(.key) (\(.value.command)) - \(.value.description)"' "$CLI_PACKAGES_FILE" 2>/dev/null
        ;;
    search)
        echo "рџ”Ќ Searching CLI packages for: $2"
        jq -r ".packages | to_entries[] | select(.key | contains(\"$2\")) | \"  \(.key) (\(.value.command)) - \(.value.description)\"" "$CLI_PACKAGES_FILE" 2>/dev/null
        ;;
    *)
        echo "Aurora-Shell CLI Installer"
        echo ""
        echo "Usage:"
        echo "  CLI install <package>  - Install CLI version of an app"
        echo "  CLI list               - List available CLI packages"
        echo "  CLI search <query>     - Search CLI packages"
        echo ""
        echo "Examples:"
        echo "  CLI install GitHub"
        echo "  CLI install Microsoft.Teams"
        ;;
esac
CLIEOF
                    chmod +x "$INSTALLED_DIR/CLI"
                    echo "вњ… CLI command installed"
                    echo "Usage: CLI install GitHub"
                    ;;
            esac
            ;;
        search)
            echo "рџ“‹ Aurora packages:"
            jq -r '.packages | to_entries[] | "  \(.key) - \(.value.description)"' "$PACKAGES_FILE" 2>/dev/null
            if [ -n "$2" ]; then
                echo ""
                echo "рџЌє Homebrew results:"
                brew search "$2" 2>/dev/null | head -10
            fi
            ;;
        list)
            echo "рџ“¦ Installed:"
            ls -1 "$INSTALLED_DIR" 2>/dev/null || echo "  (none)"
            ;;
        add)
            if [ -z "$2" ]; then
                echo "вќЊ Missing package name"
                echo ""
                echo "Usage: shell add <package-name> <download-url> [type] [description]"
                echo ""
                echo "Examples:"
                echo "  shell add my-tool https://example.com/tool.zip binary 'My custom tool'"
                echo "  shell add my-app https://example.com/app.dmg dmg 'My application'"
                echo ""
                echo "Types: binary, dmg, brew"
                return 1
            fi
            
            if [ -z "$3" ]; then
                echo "вќЊ Missing download URL"
                echo ""
                echo "Usage: shell add $2 <download-url> [type] [description]"
                echo ""
                echo "Example:"
                echo "  shell add $2 https://example.com/$2.zip binary 'Description here'"
                return 1
            fi
            
            echo "рџ“ќ Package submission:"
            echo "  Name: $2"
            echo "  URL: $3"
            echo "  Type: ${4:-binary}"
            echo "  Description: ${5:-Custom package}"
            echo ""
            echo -n "Submit for approval? (y/n): "
            read confirm
            if [ "$confirm" = "y" ]; then
                echo "рџ“¤ Adding to local registry..."
                local tmp=$(mktemp)
                jq ".packages[\"$2\"] = {\"url\": \"$3\", \"type\": \"${4:-binary}\", \"description\": \"${5:-Custom}\"}" "$PACKAGES_FILE" > "$tmp" 2>/dev/null
                mv "$tmp" "$PACKAGES_FILE"
                echo "вњ… Added $2 locally"
                echo ""
                echo "рџЊђ Opening approval form in browser..."
                local issue_url="https://github.com/YashB-byte/Aurora-Shell/issues/new?title=Package%20Request:%20$2&body=**Package%20Name:**%20$2%0A**URL:**%20$3%0A**Type:**%20${4:-binary}%0A**Description:**%20${5:-Custom}%0A%0A----%0ASubmitted%20via%20shell%20add%20command"
                open "$issue_url" 2>/dev/null || xdg-open "$issue_url" 2>/dev/null || echo "Visit: $issue_url"
            fi
            ;;
        remove)
            rm -f "$INSTALLED_DIR/$2"
            echo "вњ… Removed $2"
            ;;
        *)
            echo "Usage: shell install|search|list|add|remove"
            ;;
    esac
}

