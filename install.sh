shell() {
    case "$1" in
        install)
            local pkg="$2"
            local url=$(jq -r ".packages[\"$pkg\"].url" "$PACKAGES_FILE" 2>/dev/null)
            local type=$(jq -r ".packages[\"$pkg\"].type" "$PACKAGES_FILE" 2>/dev/null)
            
            if [ "$url" = "null" ]; then
                echo "рҹ”Қ Not in Aurora registry. Searching Homebrew..."
                if brew info "$pkg" &>/dev/null; then
                    echo "рҹ“Ұ Found in Homebrew: $pkg"
                    read -p "Install via brew? (y/n): " confirm
                    [ "$confirm" = "y" ] && brew install "$pkg" && echo "вң… Installed via Homebrew"
                else
                    echo "вқҢ Package not found in Aurora or Homebrew"
                fi
                return
            fi
            
            echo "рҹ“Ұ Installing $pkg..."
            case "$type" in
                dmg)
                    local tmp="/tmp/$pkg.dmg"
                    curl -L "$url" -o "$tmp"
                    hdiutil attach "$tmp" -quiet
                    local vol=$(ls /Volumes | grep -v Macintosh | head -1)
                    cp -R "/Volumes/$vol"/*.app ~/Applications/ 2>/dev/null
                    hdiutil detach "/Volumes/$vol" -quiet
                    rm "$tmp"
                    echo "вң… Installed to ~/Applications"
                    ;;
                binary)
                    curl -L "$url" -o "$INSTALLED_DIR/$pkg"
                    chmod +x "$INSTALLED_DIR/$pkg"
                    echo "вң… Installed to $INSTALLED_DIR"
                    ;;
                brew)
                    brew install "$pkg"
                    ;;
            esac
            ;;
        search)
            echo "рҹ“Ӣ Aurora packages:"
            jq -r '.packages | to_entries[] | "  \(.key) - \(.value.description)"' "$PACKAGES_FILE" 2>/dev/null
            if [ -n "$2" ]; then
                echo ""
                echo "рҹҚә Homebrew results:"
                brew search "$2" 2>/dev/null | head -10
            fi
            ;;
        list)
            echo "рҹ“Ұ Installed:"
            ls -1 "$INSTALLED_DIR" 2>/dev/null || echo "  (none)"
            ;;
        add)
            echo "рҹ“қ Add package: $2"
            echo "URL: $3"
            echo "Type: ${4:-binary}"
            read -p "вҡ пёҸ  Requires approval. Contact maintainer to add this package. Continue anyway? (y/n): " confirm
            if [ "$confirm" = "y" ]; then
                local tmp=$(mktemp)
                jq ".packages[\"$2\"] = {\"url\": \"$3\", \"type\": \"${4:-binary}\", \"description\": \"${5:-Custom}\"}" "$PACKAGES_FILE" > "$tmp" 2>/dev/null
                mv "$tmp" "$PACKAGES_FILE"
                echo "вң… Added $2 locally (not in official registry)"
            fi
            ;;
        remove)
            rm -f "$INSTALLED_DIR/$2"
            echo "вң… Removed $2"
            ;;
        *)
            echo "Usage: shell install|search|list|add|remove"
            ;;
    esac
}

